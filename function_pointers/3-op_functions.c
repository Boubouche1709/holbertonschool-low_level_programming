#include "calc.h"
#include <stdio.h>

/**
* op_add - add two int
* @a: first int
* @b: second int
* Return: result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - sub two int
* @a: first int
* @b: second int
* Return: result
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - sub two int
* @a: first int
* @b: second int
* Return: result
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - sub two int
* @a: first int
* @b: second int
* Return: result
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - sub two int
* @a: first int
* @b: second int
* Return: result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
